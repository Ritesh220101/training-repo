package com.aurionpro.model;

public class Save {
	private String missionName;

	public Save(String missionName) {
		super();
		this.missionName = missionName;
	}

	public String getMissionName() {
		return missionName;
	}

	@Override
	public String toString() {
		return "Save [missionName=" + missionName + "]";
	}
	
	
	
}
